public class hitungnilai {
    //fungsi hitung nilai
    public double hitung(double nilaitubes, double nilaiquiz, double nilaitugas, double nilaiuts,double nilaiuas){
        return 0.3*nilaitubes + 0.1*nilaiquiz + 0.1*nilaitugas + 0.25*nilaiuts + 0.25*nilaiuas;
    }
}
